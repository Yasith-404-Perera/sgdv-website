let lang='en';
const originals=new Map();

function storeOriginal(el){
  const key=el.getAttribute('data-i18n');
  if(!key||originals.has(el))return;
  const tag=el.tagName.toLowerCase();
  if(tag==='input'||tag==='textarea'){
    originals.set(el,{type:'ph',val:el.placeholder});
  } else if(tag==='button'&&el.type==='submit'){
    originals.set(el,{type:'text',val:el.textContent});
  } else {
    originals.set(el,{type:'html',val:el.innerHTML});
  }
}

function applyLang(newLang){
  lang=newLang;
  const isSi=lang==='si';
  document.body.classList.toggle('si',isSi);
  document.querySelectorAll('[data-i18n]').forEach(el=>{
    storeOriginal(el);
    const key=el.getAttribute('data-i18n');
    const orig=originals.get(el);
    if(!orig)return;
    if(orig.type==='ph'){
      el.placeholder=isSi?(SI[key]||orig.val):orig.val;
    } else if(orig.type==='text'){
      el.textContent=isSi?(SI[key]||orig.val):orig.val;
    } else {
      el.innerHTML=isSi?(SI[key]||orig.val):orig.val;
    }
  });
}

document.addEventListener('DOMContentLoaded',()=>{
  const langButtons=[...document.querySelectorAll('[data-lang-toggle]')];
  langButtons.forEach(btn=>btn.addEventListener('click',()=>{
    const next=lang==='en'?'si':'en';
    langButtons.forEach(item=>{item.textContent=next==='si'?'EN':'සි';});
    applyLang(next);
  }));
});
